int main(void)
{
   int cmd;

   configureSwitch(BLANK, BLANK, SDA, BLANK, BLANK, BLANK, SCL, BLANK);
   // Initialization
   init();
   // Run application
   while(1){

     // wait and store valid command
      while((MAILBOX_CMD_ADDR & 0x01)==0); 
      cmd = MAILBOX_CMD_ADDR; 

      switch(cmd){
         case SET_NORMAL_DISPLAY:
            setNormalDisplay();
            MAILBOX_CMD_ADDR = 0x0; 
            break;
            
         case SET_INVERSE_DISPLAY:
            setInverseDisplay();
            MAILBOX_CMD_ADDR = 0x0; 
            break;
            
         case SET_GRAY_LEVEL:
            setGrayLevel(0xFF);
            MAILBOX_CMD_ADDR = 0x0; 
            break;
            
         case SET_VERTICAL_MODE:   
            setVerticalMode();
            MAILBOX_CMD_ADDR = 0x0; 
            break;
            
         case SET_HORIZONTAL_MODE:
            setHorizontalMode();
            MAILBOX_CMD_ADDR = 0x0; 
            break;
            
         case SET_TEXT_XY:
            setTextXY();
            MAILBOX_CMD_ADDR = 0x0; 
            break;
            
         case CLEAR_DISPLAY:
            clearDisplay();
            MAILBOX_CMD_ADDR = 0x0; 
            break;
            
         case SET_CONTRAST_LEVEL:
            setContrastLevel();
            MAILBOX_CMD_ADDR = 0x0; 
            break;
         
         case PUT_STRING:
            putString();
            MAILBOX_CMD_ADDR = 0x0; 
            break;
            
         case SET_HORIZONTAL_SCROLL:
            setHorizontalScrollProperties();
            MAILBOX_CMD_ADDR = 0x0; 
            break;
            
         case ENABLE_SCROLL:
            activateScroll();
            MAILBOX_CMD_ADDR = 0x0; 
            break;
            
         case DISABLE_SCROLL:
            deactivateScroll();
            MAILBOX_CMD_ADDR = 0x0; 
            break;
            
         default:
            MAILBOX_CMD_ADDR = 0x0; // reset command
            break;
      }
   }
   return 0;
}
